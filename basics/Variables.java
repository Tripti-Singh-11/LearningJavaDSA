package basics;

public class Variables {
    public static void main(String[] args) {
        int x; //declaration
        x=2;  //initialization
        System.out.println(x);
        x = 7; //updation
        System.out.println(x);
        x = x + 8; // updation
        System.out.println(x);
        x = x - 5;  //updation
        System.out.println(x);
    }

}

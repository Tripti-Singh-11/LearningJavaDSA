package basics.Assignment;

public class ArithemeticOperators {
    public static void main(String[] args) {
        // Integers
        int x = 2;
        int y = 4;
        System.out.println((x+y));
        System.out.println((x-y));
        System.out.println((x*y));
        System.out.println( (x/y));
        // floats
        float a = (float)2.4;
        float b = 5.2f;
        System.out.println((a+b));
        System.out.println((a-b));
        System.out.println((a*b));
        System.out.println((a/b));
        // doubles
        double p = 11.5;
        double q = 8.5f;
        System.out.println((p+q));
        System.out.println((p-q));
        System.out.println((p*q));
        System.out.println((p/q));
    }   
}
